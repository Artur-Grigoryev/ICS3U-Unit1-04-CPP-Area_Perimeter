// This program calculates the area and perimeter of a rectangle
//    with dimensions 7cm x 3cm

#include <iostream>

int main() {
    std::cout << "If a rectangle has the dimensions: " << std::endl;
    std::cout << "7cm x 3cm" << std::endl;
    std::cout << std::endl;
    std::cout << "Area is " << (7 * 3) << "cm^2." << std::endl;
    std::cout << "Perimeter is " << (2 * (7 + 3)) << "cm." << std::endl;
    std::cout << std::endl;
    std::cout << "Done." << std::endl;
}
